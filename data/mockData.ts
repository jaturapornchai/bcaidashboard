// ฟังก์ชันสร้างข้อมูลสุ่ม
export const generateRandomData = (baseValue: number, count: number) => {
  return Array.from({ length: count }, (_, i) => ({
    ชื่อ: `สินค้า ${i + 1}`,
    ค่า: baseValue + Math.random() * baseValue * 0.5,
  }));
};

